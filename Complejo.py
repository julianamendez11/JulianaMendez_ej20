import numpy as np

class Complejo():
    def __init__(self,real,imaginario):
        self.imaginario=imaginario
        self.real=real
        
    def conjugado():
        return -imaginario
    
    def calcula_norma():
        return np.sqrt(imaginario**2 + real**2)
            
    def pow(n):
        return (real+imaginario)**n
